def generate_chatbot_reply(text, emotion="neutral"):
    # Basic reply logic
    if emotion == "happy":
        return f"😊 You sound cheerful! Let's talk more about: {text}"
    elif emotion == "sad":
        return f"😔 I'm here for you. Tell me more about: {text}"
    elif emotion == "angry":
        return f"😡 I get you're upset. I'm listening: {text}"
    else:
        return f"🙂 You said: {text}"
